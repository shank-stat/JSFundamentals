// Increment and Decrement 
// Increment
for (let i=0;i<=10; i++) {
    console.log(i);
}

// Decrement
for (let i=10;i >=0; i--) {
    console.log(i);
}

//Unary Plus
console.log("3" +3); //33
console.log(+"3" +3); //6