// Comparision Operators
// Comparision -==,===,!=,!==,>,<,<=,>=
// == - Checks only the value

// === - Strict equality (value + type)
// NOT Strict
let a = 1
let b = 1
console.log(a ==b);

// Strict
let c = 1
let d = "3"
console.log(c == d);

// !== - Strict equality (value + type)
// NOT Strict
let e = 1
let f = 1
console.log(e !== f);

// Strict
let g = 1
let h = "3"
console.log(g !== h);
 