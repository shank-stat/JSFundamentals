// Data Types Forms
//1. Literals - Actually the value of the data
//2. Variable - Assigned or holding a data
//3. Expression - Evaluate to, mostly using operators or returns statement 

//string
console.log("Hello!"); // Literal form
let str = "Hello!" // Variable form
console.log(str);
console.log("He"+"llo!"); // Expression

//boolean
console.log(true); // Literal form
let val = true // Variable form
console.log(val);
console.log(5<10); // Expression 

//Functions
function sum(num1,num2){
    return num1+num2
}

sum(7,15)
