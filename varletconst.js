// var - Declars a variable, optionally initializing it to a value - only one available before ES6, let and const added in 2015
// let - Declares a block-scoped, local variable optionally initializing it to a value - Preferred wherever possible
let val =10
console.log(val);
val =30
console.log(val);

//const - Declares a block-scoped, read only named constant.
// Const Rules -  
// 1. Must be Initialized with a value.
const APP_URLONE 
console.log(APP_URLONE);

const APP_URL ="https://www.google.com"
console.log(APP_URL);

// 2. Cannot be reassigned.
const APP_URLONE1 ="https://www.google.com"
console.log(APP_URLONE);
APP_URLONE1 ="https://www.google.com"


// 3. Declared to be capitalized - General COnvention